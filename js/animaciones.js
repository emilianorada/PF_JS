//ANIMACIONES:
function animMov(id){
    $(id).slideUp(80);
    $(id).slideDown(700);
}

function animColor1(id){
    $(id).css("background-color", "#071C2C")
         .css("color","gold") 
}

function animColor2(id){
        $(id).css("background-color", "#343a40")
             .css("color", "#f8f9fa")     
    }
//========================================================================================================================================



